Today I made a healthier oatmeal muffin, perfect for the mornings when you  <br>
need a quick breakfast on the go, but you still need something to fill you up <br>
until lunchtime. <br>

If you like oats you’ll be sure to like these muffins, here is the recipe.
<br>
<br>

<img src="foto/mengjesi/mufins2.jpg" style="width:50%;height:85%;margin-top:0%;"></img>
<br>
<br>

Oatmeal muffins
<table >
<tr><td>- 115 gram unsalted butter<td></tr>
<tr><td>- 150 gram oats</td></tr>
<tr><td>- 200 gram all-purpose flour</td></tr>
<tr><td>- 1 tsp salt</td></tr>
<tr><td>- 3/4 tsp baking powder</td></tr>
<tr><td>- 1/4 tsp baking soda</td></tr>
<tr><td>- 265 gram light brown soft sugar</td></tr>
<tr><td>- 400 ml whole milk</td></tr>
<tr><td>- 2 eggs</td></tr>


</table>
<br>


<img src="foto/mengjesi/mufins5.jpg" style="width:50%;height:65%;margin-top:0%;"></img>

<br>
Topping
<table >
<tr><td>- 45 gram oats<td></tr>
<tr><td>- 40 gram all-purpose flou</td></tr>
<tr><td>- 50 gram walnuts and pecans chopped</td></tr>
<tr><td>- 65 gram light brown soft suga</td></tr>
<tr><td>- 1 tsp ground cinnamon</td></tr>
<tr><td>- 1/8 tsp salt</td></tr>
<tr><td>- 55 gram butter melted</td></tr>


</table>

<br>
<br>

<img src="foto/mengjesi/mufins3.jpg" style="width:50%;height:65%;margin-top:0%;"></img>

<br>
<br>

1.Put 30 grams (2 tablespoons) of the butter in a frying pan with the oats.
<br>2.Fry the oats in the butter on a medium heat for about 6-8 minutes until softened. When it starts to brown and smell a bit like popcorn, it’s ready.
<br>3.Put the oats in a food processor and whizz until quite finely ground.
<br>4.Add the flour, baking powder, baking soda and salt and whizz for another 30 seconds.
<br>
<img src="foto/mengjesi/mufins6.jpg" style="width:50%;height:65%;margin-top:0%;"></img>

<br>
Oatmeal muffins & topping
<br>
<br>
1.Top each of the filled cupcake cases with 1-2 tablespoons of the topping. Bake in a 190 C/375 F oven for about 18-25 minutes.
<br>
2.Freshly baked and ready to be devoured… The kitchen smells amazing!
<br>3.Breakfast oatmeal muffins – baked with love.
<br>4.If I don’t have light brown soft sugar at home I use regular sugar instead. I also like to add 1 or 2 mashed bananas, just add them at the very end when making the batter. The muffins on the picture are made with regular sugar and banana, also very good.
